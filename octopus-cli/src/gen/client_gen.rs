//! TypeScript client SDK generation
//!
//! Generates:
//! - Namespace-chained REST client (client.twinos.users.list())
//! - Fully typed DTOs from Octopus Schema
//! - TanStack Query hooks
//! - Per-service standalone packages
//! - Core fetcher with auth support

use crate::gen::types::*;
use anyhow::{Context, Result};
use std::collections::{BTreeMap, HashMap};
use tracing::info;

/// Generate the full TypeScript client SDK
pub fn generate_client(
    opts: &ClientGenOptions,
    schema: &OctopusSchema,
    gen_config: &GenConfig,
) -> Result<()> {
    let output_dir = &opts.output_dir;
    std::fs::create_dir_all(output_dir)?;

    // Generate core infrastructure
    generate_core_types(output_dir)?;
    generate_fetcher(output_dir, opts)?;

    // Check if any service has channels (WS/SSE)
    let has_ws_channels = schema.services.values().any(|s| {
        s.channels.values().any(|c| c.protocol == "websocket")
    });
    let has_sse_channels = schema.services.values().any(|s| {
        s.channels.values().any(|c| c.protocol == "sse")
    });

    // Generate WS/SSE runtimes if needed
    if has_ws_channels {
        let ws_enabled = opts.websocket.as_ref().map(|w| w.enabled).unwrap_or(true);
        if ws_enabled {
            generate_ws_client(output_dir)?;
        }
    }
    if has_sse_channels {
        let sse_enabled = opts.sse.as_ref().map(|s| s.enabled).unwrap_or(true);
        if sse_enabled {
            generate_sse_client(output_dir)?;
        }
    }

    // Generate DTO types (includes channel union types)
    let type_transforms = gen_config.client.as_ref().and_then(|c| c.types.as_ref());
    generate_types_file(output_dir, &schema.types, type_transforms)?;

    // Generate per-service modules
    let mut service_names = Vec::new();
    for (service_name, service_schema) in &schema.services {
        generate_service_module(output_dir, service_name, service_schema, opts)?;
        service_names.push(service_name.clone());
    }
    service_names.sort();

    // Generate main index.ts
    generate_main_index(output_dir, &service_names, schema, opts)?;

    // Generate TanStack Query hooks
    if let Some(ref tq) = opts.tanstack_query {
        if tq.enabled {
            std::fs::create_dir_all(format!("{output_dir}/tanstack"))?;
            for (service_name, service_schema) in &schema.services {
                generate_tanstack_hooks(output_dir, service_name, service_schema, tq)?;
            }
            generate_tanstack_index(output_dir, &service_names)?;
        }
    }

    // Generate per-service packages
    if opts.per_service_packages {
        for (service_name, service_schema) in &schema.services {
            generate_per_service_package(output_dir, service_name, service_schema, opts, schema)?;
        }
    }

    // Generate package.json
    generate_package_json(output_dir, opts)?;

    info!(output_dir = %output_dir, "TypeScript client SDK generated");
    Ok(())
}

