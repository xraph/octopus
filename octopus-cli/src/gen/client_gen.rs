        ts.push_str("      ws.on('error', options.onError);\n");
        ts.push_str("    }\n\n");
        ts.push_str("    return () => {\n");
        ts.push_str("      unsubOpen(); unsubClose(); unsubMsg();\n");
        ts.push_str("      ws.disconnect();\n");
        ts.push_str("      wsRef.current = null;\n");
        ts.push_str("      setIsConnected(false);\n");
        ts.push_str("    };\n");
        ts.push_str("  }, [config.baseURL, options?.enabled]);\n\n");
