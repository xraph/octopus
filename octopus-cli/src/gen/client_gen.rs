        ts.push_str("    const unsubOpen = ws.on('open', () => setIsConnected(true));\n");
        ts.push_str("    const unsubClose = ws.on('close', () => setIsConnected(false));\n");
        ts.push_str("    const unsubMsg = ws.on('message', (data) => {\n");
        ts.push_str("      setLastMessage(data as any);\n");
        ts.push_str("      onMessageRef.current?.(data as any);\n");
        ts.push_str("    });\n");
