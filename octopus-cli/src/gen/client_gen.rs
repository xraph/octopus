fn generate_fetcher(output_dir: &str, opts: &ClientGenOptions) -> Result<()> {
    let dir = format!("{output_dir}/core");

    let auth_header = opts
        .auth
        .as_ref()
        .map(|a| a.header.as_str())
        .unwrap_or("Authorization");
    let auth_prefix = opts
        .auth
        .as_ref()
        .map(|a| a.prefix.as_str())
        .unwrap_or("Bearer ");

    let content = format!(
        r#"// Auto-generated by octopus gen — do not edit

import type {{
  AuthConfig,
  ClientConfig,
  Cookies,
  RequestInterceptor,
  ResponseInterceptor,
  ResponseType,
}} from './types';
import {{ ApiError }} from './types';

const AUTH_HEADER = '{auth_header}';
const AUTH_PREFIX = '{auth_prefix}';

// ── Helpers ──────────────────────────────────────────────────────────────────

/** Build auth header value from AuthConfig */
function buildAuthHeader(auth: AuthConfig | undefined): string | undefined {{
  if (!auth) return undefined;
  if (auth.headerValue) return auth.headerValue;
  if (auth.token) return `${{AUTH_PREFIX}}${{auth.token}}`;
  if (auth.apiKey) return auth.apiKey;
  return undefined;
}}

/** Serialize cookies to a Cookie header string */
function buildCookieHeader(
  clientCookies?: Cookies,
  requestCookies?: Cookies,
): string | undefined {{
  const merged = {{ ...clientCookies, ...requestCookies }};
  const entries = Object.entries(merged);
  if (entries.length === 0) return undefined;
  return entries.map(([k, v]) => `${{k}}=${{v}}`).join('; ');
}}

/** Build URL with path and query params */
function buildURL(
  baseURL: string,
  path: string,
  pathParams?: Record<string, string>,
  queryParams?: Record<string, unknown>,
): string {{
  let url = `${{baseURL}}${{path}}`;

  // Replace path parameters
  if (pathParams) {{
    for (const [key, value] of Object.entries(pathParams)) {{
      url = url.replace(`{{${{key}}}}`, encodeURIComponent(String(value)));
    }}
  }}

  // Add query parameters
  if (queryParams) {{
    const params = new URLSearchParams();
    for (const [key, value] of Object.entries(queryParams)) {{
      if (value !== undefined && value !== null) {{
        if (Array.isArray(value)) {{
          // Arrays: ?ids=1&ids=2
          for (const item of value) {{
            params.append(key, String(item));
          }}
        }} else {{
          params.append(key, String(value));
        }}
      }}
    }}
    const qs = params.toString();
    if (qs) url += `?${{qs}}`;
  }}

  return url;
}}

/** Create an AbortSignal with a timeout */
function timeoutSignal(ms: number, existing?: AbortSignal): AbortSignal {{
  const controller = new AbortController();

  const timer = setTimeout(() => controller.abort(new Error(`Request timed out after ${{ms}}ms`)), ms);

  // If the existing signal fires first, abort ours too
  if (existing) {{
    if (existing.aborted) {{
      controller.abort(existing.reason);
    }} else {{
      existing.addEventListener('abort', () => controller.abort(existing.reason), {{ once: true }});
    }}
  }}

  // Clear timer when the signal fires
  controller.signal.addEventListener('abort', () => clearTimeout(timer), {{ once: true }});

  return controller.signal;
}}

/** Parse response based on expected type */
async function parseResponse<T>(
  response: Response,
  responseType: ResponseType = 'json',
): Promise<T> {{
  switch (responseType) {{
    case 'text':
      return (await response.text()) as T;
    case 'blob':
      return (await response.blob()) as T;
    case 'arrayBuffer':
      return (await response.arrayBuffer()) as T;
    case 'stream':
      return response.body as T;
    case 'json':
    default:
      return (await response.json()) as T;
  }}
}}

// ── Core Request ─────────────────────────────────────────────────────────────

export interface RequestOptions {{
  pathParams?: Record<string, string>;
  queryParams?: Record<string, unknown>;
  body?: unknown;
  auth?: AuthConfig;
  headers?: Record<string, string>;
  cookies?: Cookies;
  credentials?: RequestCredentials;
  responseType?: ResponseType;
  signal?: AbortSignal;
  timeout?: number;
}}

/** Core fetcher — all generated methods delegate to this */
export async function request<T>(
  config: ClientConfig,
  method: string,
  path: string,
  options?: RequestOptions,
): Promise<T> {{
  const fetchFn = config.fetch ?? globalThis.fetch;
  const url = buildURL(config.baseURL, path, options?.pathParams, options?.queryParams);

  // ── Headers ────────────────────────────────────────────────────────────
  const headers: Record<string, string> = {{
    'Content-Type': 'application/json',
    'Accept': 'application/json',
    ...config.headers,
    ...options?.headers,
  }};

  // Auth: per-request overrides client-level
  const authValue = buildAuthHeader(options?.auth ?? config.auth);
  if (authValue) {{
    headers[AUTH_HEADER] = authValue;
  }}

  // Cookies: merge client + per-request, serialize to Cookie header
  const cookieHeader = buildCookieHeader(config.cookies, options?.cookies);
  if (cookieHeader) {{
    headers['Cookie'] = cookieHeader;
  }}

  // ── Signal / Timeout ───────────────────────────────────────────────────
  const timeout = options?.timeout ?? config.timeout;
  let signal = options?.signal;
  if (timeout && timeout > 0) {{
    signal = timeoutSignal(timeout, signal);
  }}

  // ── Credentials ────────────────────────────────────────────────────────
  const credentials = options?.credentials ?? config.credentials;

  // ── Build RequestInit ──────────────────────────────────────────────────
  let init: RequestInit = {{
    method,
    headers,
    body: options?.body !== undefined ? JSON.stringify(options.body) : undefined,
    signal,
    credentials,
  }};

  // ── Request Interceptors ───────────────────────────────────────────────
  if (config.onRequest) {{
    for (const interceptor of config.onRequest) {{
      init = await interceptor(url, init);
    }}
  }}

  // ── Execute ────────────────────────────────────────────────────────────
  let response = await fetchFn(url, init);

  // ── Response Interceptors ──────────────────────────────────────────────
  if (config.onResponse) {{
    for (const interceptor of config.onResponse) {{
      response = await interceptor(response, {{ url, init }});
    }}
  }}

  // ── Error Handling ─────────────────────────────────────────────────────
  if (!response.ok) {{
    const body = await response.text().catch(() => '');
    let parsed: unknown;
    try {{ parsed = JSON.parse(body); }} catch {{ parsed = body; }}
    throw new ApiError(
      `${{method}} ${{path}} failed with status ${{response.status}}`,
      response.status,
      parsed,
      response.headers,
    );
  }}

  // Handle no-content responses
  if (response.status === 204) {{
    return undefined as T;
  }}

  // ── Parse Response ─────────────────────────────────────────────────────
  return parseResponse<T>(response, options?.responseType ?? 'json');
}}
"#
    );

    write_ts_file(&format!("{dir}/fetcher.ts"), &content)
}

